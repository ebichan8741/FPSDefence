//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CTitleBG::CTitleBG(int nPriolity) :CScene2D(nPriolity)
{
}

//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
HRESULT CTitleBG::Init(void)
{
    m_vPos = D3DXVECTOR3(0.0f + SCREEN_WIDTH * 0.5f, 0.0f + SCREEN_HEIGHT * 0.5f, 0.0f);
    m_vSize = D3DXVECTOR3(SCREEN_WIDTH, SCREEN_HEIGHT, 0.0f);

    CScene2D::Init();
    return S_OK;
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CTitleBG::Uninit(void)
{
    CScene2D::Uninit();
}

//*************************************************************************************************
// 更新処理
//*************************************************************************************************
void CTitleBG::Update(void)
{
    m_TexPos.x -= 0.01f;
    CScene2D::Update();
}

//*************************************************************************************************
// 描画処理
//*************************************************************************************************
void CTitleBG::Draw(void)
{
    CScene2D::Draw();
}

//*************************************************************************************************
// インスタンス生成
//*************************************************************************************************
CTitleBG *CTitleBG::Create(void)
{
    CTitleBG *titleBG;
    titleBG = new CTitleBG(PRIOLITY_0);
    titleBG->Init();

    titleBG->LoadTexture(TEXTURE_TITLEBG);
    //titleBG->m_pTexture = CManager::GetTextureManager()->getResource(CManager::GetTextureManager()->getTextureName(TEXTURE_TITLE));

    return titleBG;
}
