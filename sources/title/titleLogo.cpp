//=================================================================================================
// 定数定義
//=================================================================================================
#define LOGO_SIZE_X	(500.0f)		// 幅
#define LOGO_SIZE_Y	(280.0f)		// 高さ

//=================================================================================================
// 構造体宣言
//=================================================================================================

//=================================================================================================
// グローバル変数
