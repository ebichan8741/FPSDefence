//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CTitleLogo::CTitleLogo(int nPriolity) :CScene2D(nPriolity)
{
}

//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
HRESULT CTitleLogo::Init(void)
{
    m_vPos = D3DXVECTOR3(LOGO_POS_X, LOGO_POS_Y, 0.0f);
    m_vSize = D3DXVECTOR3(LOGO_SIZE_X, LOGO_SIZE_Y, 0.0f);

    CScene2D::Init();
    return S_OK;
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CTitleLogo::Uninit(void)
{
    CScene2D::Uninit();
}

//*************************************************************************************************
// 更新処理
//*************************************************************************************************
void CTitleLogo::Update(void)
{

}

//*************************************************************************************************
// 描画処理
//*************************************************************************************************
void CTitleLogo::Draw(void)
{
    CScene2D::Draw();
}

//*************************************************************************************************
// インスタンス生成
//*************************************************************************************************
CTitleLogo *CTitleLogo::Create(void)
{
    CTitleLogo *titleLogo;
    titleLogo = new CTitleLogo(PRIOLITY_2);
    titleLogo->Init();

    titleLogo->LoadTexture(TEXTURE_TITLELOGO);
    //titleLogo->m_pTexture = CManager::GetTextureManager()->getResource(CManager::GetTextureManager()->getTextureName(TEXTURE_TITLELOGO));

    return titleLogo;
}
