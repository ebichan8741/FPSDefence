#define CURSOL_WIDTH     (20)   //枠の幅
#define CURSOL_HEIGHT    (20)   //枠の高さ

//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CReticle::CReticle(int nPriolity) :CScene2D(nPriolity)
{
    m_vPos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
    m_vSize = D3DXVECTOR3(50.0f, 50.0f, 0.0f);
}

//*************************************************************************************************
// デストラクタ
//*************************************************************************************************
CReticle::~CReticle(){}

//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
HRESULT CReticle::Init(void)
{
    CScene2D::Init();
    SetColor(D3DXCOLOR(0.0f, 1.0f, 0.0f, 1.0f));
    return S_OK;
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CReticle::Uninit(void)
{
    CScene2D::Uninit();
}

//*************************************************************************************************
// 更新処理
//*************************************************************************************************
void CReticle::Update(void)
{
    m_vPos = CManager::GetInputMouse()->GetMousePos();
    CScene2D::Update();
}

//*************************************************************************************************
// 描画処理
//*************************************************************************************************
void CReticle::Draw(void)
{
    CScene2D::Draw();
}

//*************************************************************************************************
// インスタンス生成
//*************************************************************************************************
CReticle *CReticle::Create(void)
{
    CReticle *pReticle;

    //インスタンス生成
    pReticle = new CReticle(PRIOLITY_2);

    //初期化処理
    pReticle->Init();

    //モデル読み込み
    pReticle->LoadTexture(TEXTURE_RETICLE);
    //pMouseCursol->m_pTexture = CManager::GetTextureManager()->getResource(CManager::GetTextureManager()->getTextureName(TEXTURE_MOUSECURSOL));
    return pReticle;
}