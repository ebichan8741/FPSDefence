//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
HRESULT CResultMode::Init(void)
{
    CManager::mode = MODE_RESULT;

    CResultBG::Create();

    return 0;
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CResultMode::Uninit(void)
{
    //オブジェクトの解放
    CScene::ReleaseAll();
}

//*************************************************************************************************
// 更新処理
//*************************************************************************************************
void CResultMode::Update(void)
{
    if (CManager::GetInputKeyboard()->GetKeyTrigger(DIK_RETURN)) {
        CFade::SetFade(FADE_OUT, new CTitleMode);
    }
}

